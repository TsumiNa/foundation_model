#!/usr/bin/env python3
"""Generate the task-scaling experiment configs from the replay-sweep base config.

Derives the shared sections ([data] .. [training.logging], incl. the full 24-task catalog) from
../rikyu_replay_sweep/configs/sweep_n1000.toml and appends the per-config command sections:

- pre_n1000.toml : pretrain the 21 non-dielectric tasks with replay n=1000, task_order=random.
  task_order_groups pins formation_energy to step 1 (it is in the inverse objective, so every
  step checkpoint k>=1 supports the inverse stage) and the 7 kernel-regression tasks to a final
  block (KR replay dominates wall time). task_order_seed=7501; per-run jobs override
  --seed 2025+ORD / task_order_seed 7501+ORD to replicate what n_runs=3 would produce, but in
  parallel Slurm jobs.
- ws_n1000.toml  : warm-start continuation adding the 3 dielectric targets jointly (fixed order
  total -> ionic -> electronic) with the same replay n; checkpoint via --checkpoint. Without
  --checkpoint it trains the 3 targets from scratch (the k=0 baseline).
- ft.toml        : frozen-encoder finetune of the 3 dielectric targets, epochs=100 with the
  shared early-stopping config (default 20 would confound "better pretraining" with
  "undertrained").
- inv.toml       : inverse design on a ws/ft output model. Pure regression objective
  (formation_energy -> -1.0, the 3 dielectrics -> +1.0, z-scored units), 2 paths (latent default
  + composition with max_elements=4 and low diversity_scale=0.2), top_objective seeds, no
  animations (the aggregated HTML viewer is built later from the npz trajectories).

Rerun after editing: .venv/bin/python experiments/rikyu_task_scaling/make_configs.py
"""

from pathlib import Path

HERE = Path(__file__).resolve().parent
BASE = HERE.parent / "rikyu_replay_sweep/configs/sweep_n1000.toml"
OUT = HERE / "configs"

REPLAY_NS = [1000, 1500]
PINNED_FIRST = ["formation_energy"]
NON_KR_REST = [
    "density", "efermi", "final_energy", "total_magnetization", "volume",
    "magnetization", "curie", "neel", "magnetic_moment",
    "tc", "kp", "klat", "material_type",
]  # fmt: skip
KR = [
    "magnetic_susceptibility", "zt", "power_factor", "thermal_conductivity",
    "electrical_resistivity", "dos_density", "seebeck",
]  # fmt: skip
TARGETS = ["dielectric_total", "dielectric_ionic", "dielectric_electronic"]


def toml_list(items: list[str], indent: str = "    ") -> str:
    return "[\n" + "\n".join(f'{indent}"{t}",' for t in items) + "\n]"


def shared_block(*, with_training: bool = True) -> str:
    text = BASE.read_text()
    start = text.index("[data]")
    end = text.index("[pretrain]") if with_training else text.index("[training]")
    return text[start:end].rstrip() + "\n"


def header(lines: list[str]) -> str:
    return "".join(f"# {line}\n" for line in lines) + "#\n# GENERATED by make_configs.py — edit that script, not this file.\n\n"


def pre_config(replay_n: int) -> str:
    return (
        header(
            [
                f"fm pretrain — task-scaling base run: 21 tasks (24 minus the 3 dielectric targets), replay n={replay_n}.",
                "task_order=random per run; groups pin formation_energy to step 1 (inverse objective needs its",
                "head at every k) and the 7 kernel-regression tasks to a final block (KR replay dominates wall",
                "time). Jobs override per run:",
                "  --seed $((2025+ORD)) --set pretrain.task_order_seed=$((7501+ORD)) --output-dir ...",
                "Every step's checkpoint (training/stepNN_<task>/checkpoint.pt) feeds the ws/ft/inverse stages.",
            ]
        )
        + shared_block()
        + f"""
[pretrain]
task_sequence = {toml_list(PINNED_FIRST + NON_KR_REST + KR)}
n_runs = 1
task_order = "random"
task_order_seed = 7501
task_order_groups = [
    {toml_list(PINNED_FIRST, indent="        ")},
    {toml_list(NON_KR_REST, indent="        ")},
    {toml_list(KR, indent="        ")},
]

[pretrain.replay]
interval = 1
amount = {replay_n}

[output]
dir = "artifacts/task_scaling/pre_adhoc"
"""
    )


def ws_config(replay_n: int) -> str:
    return (
        header(
            [
                f"fm pretrain — warm-start continuation: add the 3 dielectric targets jointly, replay n={replay_n}.",
                "Checkpoint (a pre_o* step checkpoint) via --checkpoint; without one this trains the 3 targets",
                "from scratch (the k=0 baseline). Fixed target order: total -> ionic -> electronic.",
            ]
        )
        + shared_block()
        + f"""
[pretrain]
task_sequence = {toml_list(TARGETS)}
n_runs = 1
task_order = "fixed"

[pretrain.replay]
interval = 1
amount = {replay_n}

[output]
dir = "artifacts/task_scaling/ws_adhoc"
"""
    )


def ft_config() -> str:
    return (
        header(
            [
                "fm finetune — frozen-encoder finetune of the 3 dielectric targets from a pre_o* step",
                "checkpoint (--checkpoint). epochs=100 + early stopping (the default 20 risks undertraining,",
                "which would confound the task-scaling signal).",
            ]
        )
        + shared_block()
        + f"""
[finetune]
tasks = {toml_list(TARGETS)}
epochs = 100
freeze_encoder = true
add_new_tasks = true

[output]
dir = "artifacts/task_scaling/ft_adhoc"
"""
    )


def inv_config() -> str:
    scenarios = "".join(
        f"""
[[inverse.scenarios]]
name = "fe_down_{short}_up"

[[inverse.scenarios.targets]]
task = "formation_energy"
value = -1.0

[[inverse.scenarios.targets]]
task = "{target}"
value = 1.0
"""
        for target, short in (
            ("dielectric_total", "total"),
            ("dielectric_ionic", "ionic"),
            ("dielectric_electronic", "electronic"),
        )
    )
    return (
        header(
            [
                "fm inverse — design quality probe on a ws/ft output model (--checkpoint).",
                "THREE scenarios (pure regression, z-scored units): formation_energy down 1 sigma paired",
                "with EACH dielectric target up 1 sigma separately. The shared seed pool comes from the",
                "first scenario's objective (engine design). Two paths: latent (default alignment 0.5) and",
                "composition with max_elements=4 and diversity_scale=0.2 (low per-recipe element diversity).",
                "No animations in batch runs — trajectories land in npz and feed the HTML viewer.",
            ]
        )
        + shared_block(with_training=False)
        + f"""
[inverse]
steps = 300
lr = 0.05
record_trajectory = true
per_seed_trajectories = false
animation_formats = []
seed = 2025

[inverse.seeds]
strategy = "top_objective"
n = 20
split = "test"
dedup_by_element_system = true
{scenarios}
[[inverse.paths]]
name = "latent_default"
method = "latent"

[[inverse.paths]]
name = "comp_k4_lowdiv"
method = "composition"
init = "seed"
max_elements = 4
diversity_scale = 0.2

[output]
dir = "artifacts/task_scaling/inv_adhoc"
"""
    )


def main() -> None:
    OUT.mkdir(exist_ok=True)
    files = {"ft.toml": ft_config(), "inv.toml": inv_config()}
    for n in REPLAY_NS:
        files[f"pre_n{n}.toml"] = pre_config(n)
        files[f"ws_n{n}.toml"] = ws_config(n)
    for name, content in files.items():
        (OUT / name).write_text(content)
        print(f"wrote {OUT / name}")


if __name__ == "__main__":
    main()
